package abc;

public class CovertingStringintoPrimitiveDatatype {

	public static void main(String[] args) {
		int i = 100; //integer to float
		float f = i; //converted integer into float
		double d = f; // converted float into double
		
		System.out.println("Integer value"+i);
		System.out.println("Float value"+f);
		System.out.println("Double value"+d);

	}

}
